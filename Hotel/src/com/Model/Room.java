package com.Model;
/*
 * 
 * 房间信息
 * */
 
public class Room {
	
	private String IDRoom;
	private String TypeRoom;
	private String SubclassRoom;
	private int PriceRoom;
	
	public String getIDRoom() {
		return IDRoom;
	}
	public void setIDRoom(String iDRoom) {
		IDRoom = iDRoom;
	}
	public String getTypeRoom() {
		return TypeRoom;
	}
	public void setTypeRoom(String typeRoom) {
		TypeRoom = typeRoom;
	}
	public String getSubclassRoom() {
		return SubclassRoom;
	}
	public void setSubclassRoom(String subclassRoom) {
		SubclassRoom = subclassRoom;
	}
	public int getPriceRoom() {
		return PriceRoom;
	}
	public void setPriceRoom(int priceRoom) {
		PriceRoom = priceRoom;
	}
	
	
}
